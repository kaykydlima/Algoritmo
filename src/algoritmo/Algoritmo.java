
package algoritmo;

public class Algoritmo {

    public static void main(String[] args) {
        //Declaração váriavel com valor "X" e "Y"
        String letra = "X";
        String letra2 = "Y";
        //Input das váriaveis
        System.out.println(letra);
        System.out.println(letra2);
        //Concatenação das váriaveis com valores "X" e "Y"
        String s = letra + letra2;
        //Exibição da concatenação
        System.out.println(s);
    }
    
}
